    fun forClass(klass: String): Monograph {
        val key = when (klass) {
            "ccb_benz" -> "ccb_dihydro"
            "digoxin_class" -> "nitrate"
            "opioid_codeine", "opioid_weak" -> "cough_antitussive"
            "dpp4i", "glp1", "sglt2" -> "metformin"
            "tinidazole" -> "metronidazole"
            "antibiotic_tet" -> "antibiotic_macrolide"
            "antiviral_hep" -> "antiviral_grip"
            "melatonin" -> "general_otc"
            "tamsulosin", "finasteride", "allopurinol", "colchicine", "amiodarone", "sildenafil" -> "general_otc"
            else -> klass
        }
        return MAP[key] ?: MAP.getValue("general_otc")
    }

